	function leadingZeros3($innumber)
	{
		$outstring = strval($innumber);
		if ($innumber < 10)
		{
			$outstring = "0" . $outstring;
		}
		if ($innumber < 100)
		{
			$outstring = "0" . $outstring;
		}
		return $outstring;
	}
